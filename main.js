const langToggle = document.getElementById("langToggle");
const translatableNodes = document.querySelectorAll("[data-i18n]");
let currentLang = "es";

function applyTranslations(lang) {
  const dict = translations[lang];
  translatableNodes.forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (dict[key]) {
      node.textContent = dict[key];
    }
  });
  document.documentElement.lang = lang;
}

if (langToggle) {
  langToggle.addEventListener("click", () => {
    currentLang = currentLang === "es" ? "en" : "es";
    applyTranslations(currentLang);
  });
}

const revealElements = document.querySelectorAll(".reveal");
const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("revealed");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.18 }
);

revealElements.forEach((el, index) => {
  el.style.transitionDelay = `${Math.min(index * 60, 260)}ms`;
  revealObserver.observe(el);
});

const counters = document.querySelectorAll(".counter");
const counterObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) {
        return;
      }
      const node = entry.target;
      const targetRaw = node.getAttribute("data-target") || "0";
      const target = Number.parseInt(targetRaw, 10);
      const hasPercent = node.textContent.trim().includes("%");
      let value = 0;
      const step = Math.max(1, Math.ceil(target / 40));
      const timer = window.setInterval(() => {
        value += step;
        if (value >= target) {
          value = target;
          window.clearInterval(timer);
        }
        const suffix = hasPercent ? "%" : "+";
        node.textContent = `${value}${suffix}`;
      }, 26);
      counterObserver.unobserve(node);
    });
  },
  { threshold: 0.5 }
);

counters.forEach((counter) => counterObserver.observe(counter));

const yearNode = document.getElementById("year");
if (yearNode) {
  yearNode.textContent = String(new Date().getFullYear());
}
applyTranslations(currentLang);

// ---- Text rotator for hero kicker ----
const rotateEl = document.getElementById("heroRotate");
const rotateTerms = {
  es: ["Marca personal", "Estrategia", "Crecimiento", "Fundador", "Consultor"],
  en: ["Personal brand", "Strategy", "Growth", "Founder", "Consultant"]
};
let rotateIndex = 0;

function rotateTerm() {
  if (!rotateEl) return;
  const terms = rotateTerms[currentLang];
  // slide out
  rotateEl.classList.remove("slide-in");
  rotateEl.classList.add("slide-out");
  setTimeout(() => {
    rotateIndex = (rotateIndex + 1) % terms.length;
    rotateEl.textContent = terms[rotateIndex];
    rotateEl.classList.remove("slide-out");
    rotateEl.classList.add("slide-in");
  }, 340);
}

setInterval(rotateTerm, 2800);

// re-sync rotate text on language toggle
if (langToggle) {
  langToggle.addEventListener("click", () => {
    if (rotateEl) {
      const terms = rotateTerms[currentLang];
      rotateEl.textContent = terms[rotateIndex % terms.length];
    }
  });
}
